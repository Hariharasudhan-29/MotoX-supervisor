  Widget _buildTextField({
    required String label,
    required TextEditingController controller,
    required String hint,
    int maxLines = 1,
    bool isMono = false,
    bool isOptional = false,
    TextInputType keyboardType = TextInputType.text,
    String? errorKey,
  }) {
    final theme = Theme.of(context);
    final isDark = theme.brightness == Brightness.dark;
    final hasError = errorKey != null && _fieldErrors.containsKey(errorKey);
    final errorMsg = hasError ? _fieldErrors[errorKey]! : null;
    final errorColor = const Color(0xFFEF4444);

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Row(
          children: [
            Text(
              label,
              style: const TextStyle(fontSize: 11, fontWeight: FontWeight.bold, letterSpacing: 0.5),
            ),
            if (isOptional)
              const Text(
                '  (Optional)',
                style: TextStyle(fontSize: 10, color: Colors.grey, fontWeight: FontWeight.normal),
              )
            else
              const Text(' *', style: TextStyle(fontSize: 11, color: Color(0xFFEF4444), fontWeight: FontWeight.bold)),
          ],
        ),
        const SizedBox(height: 6),
        TextFormField(
          controller: controller,
          maxLines: maxLines,
          keyboardType: keyboardType,
          onChanged: (_) {
            if (hasError) setState(() => _fieldErrors.remove(errorKey));
          },
          style: TextStyle(
            fontSize: 13.5,
            fontFamily: isMono ? 'JetBrains Mono' : null,
          ),
          decoration: InputDecoration(
            hintText: hint,
            errorText: errorMsg,
            errorStyle: TextStyle(color: errorColor, fontSize: 10.5, fontWeight: FontWeight.w600),
            filled: true,
            fillColor: hasError
                ? errorColor.withOpacity(0.05)
                : Colors.black.withOpacity(isDark ? 0.1 : 0.02),
            contentPadding: const EdgeInsets.symmetric(horizontal: 14, vertical: 12),
            border: OutlineInputBorder(
              borderSide: BorderSide(
                color: hasError ? errorColor : (isDark ? Colors.white.withOpacity(0.08) : Colors.black.withOpacity(0.08)),
              ),
              borderRadius: BorderRadius.circular(10),
            ),
            enabledBorder: OutlineInputBorder(
              borderSide: BorderSide(
                color: hasError ? errorColor : (isDark ? Colors.white.withOpacity(0.08) : Colors.black.withOpacity(0.08)),
                width: hasError ? 1.5 : 1.0,
              ),
              borderRadius: BorderRadius.circular(10),
            ),
            focusedBorder: OutlineInputBorder(
              borderSide: BorderSide(
                color: hasError ? errorColor : theme.colorScheme.primary,
                width: 1.5,
              ),
              borderRadius: BorderRadius.circular(10),
            ),
          ),
        ),
      ],
    );
  }

