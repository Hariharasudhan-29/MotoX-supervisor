  Widget _buildStepContent(Color surface, Color border, bool isDark) {
    switch (_currentStep) {
      case 1:
        return _buildCustomerStepContent(surface, border, isDark);
      case 2:
        return _buildVehicleStepContent(surface, border, isDark);
      case 3:
        return _buildStep3(surface, border, isDark);
      case 4:
        return _buildStep4(surface, border, isDark);
      case 5:
        return _buildStep5(surface, border, isDark);
      default:
        return const SizedBox();
    }
  }

  Widget _responsiveRow(BuildContext context, List<Widget> children, {double spacing = 12.0}) {
    final isMobile = MediaQuery.of(context).size.width < 600;
    if (isMobile) {
      return Column(
        crossAxisAlignment: CrossAxisAlignment.stretch,
        children: [
          for (int i = 0; i < children.length; i++) ...[
            children[i],
            if (i < children.length - 1) SizedBox(height: spacing),
          ]
        ],
      );
    } else {
      return Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          for (int i = 0; i < children.length; i++) ...[
            Expanded(child: children[i]),
            if (i < children.length - 1) SizedBox(width: spacing),
          ]
        ],
      );
    }
  }

