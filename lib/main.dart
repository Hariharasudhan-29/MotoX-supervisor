  void _lookupVehicle() {
    if (_regController.text.isEmpty) {
      setState(() {
        _lookupStatus = 'Please enter registration number';
      });
      return;
    }
    setState(() {
      _isLookupLoading = true;
      _lookupStatus = 'Looking up vehicle database...';
    });

    Future.delayed(const Duration(milliseconds: 1200), () {
      if (mounted) {
        setState(() {
          _isLookupLoading = false;
          _lookupStatus = 'Vehicle details populated!';
          _makeController.text = 'Suzuki';
          _modelController.text = 'Avenis';
          _yearController.text = '2025';
          _colorController.text = 'Black';
          _chassisController.text = 'MB8EA12MAS8243281';
          _odoController.text = '10000';
          _engineController.text = 'AF21E-8243281';
        });
      }
    });
  }

  void _nextStep() {
    if (!_validateCurrentStep()) return;
    if (_currentStep < _totalSteps) {
      setState(() {
        _currentStep++;
      });
    } else {
      _submitJobCard();
    }
  }

  bool _validateCurrentStep() {
    final errors = <String, String>{};

    if (_currentStep == 1) {
      if (_custNameController.text.trim().isEmpty) errors['custName'] = 'Full name is required';
      if (_custPhoneController.text.trim().isEmpty) errors['custPhone'] = 'Mobile number is required';
      else if (_custPhoneController.text.trim().length < 10) errors['custPhone'] = 'Enter a valid 10-digit number';
      if (_custAddressController.text.trim().isEmpty) errors['custAddress'] = 'Address is required';
      if (_saNameController.text.trim().isEmpty) errors['saName'] = 'Service advisor name is required';
      if (_techNameController.text.trim().isEmpty) errors['techName'] = 'Technician name is required';
    }

    if (_currentStep == 2) {
      if (_regController.text.trim().isEmpty) errors['reg'] = 'Registration number is required';
      if (_makeController.text.trim().isEmpty) errors['make'] = 'Make / Brand is required';
      if (_modelController.text.trim().isEmpty) errors['model'] = 'Model is required';
      if (_yearController.text.trim().isEmpty) errors['year'] = 'Year is required';
      if (_colorController.text.trim().isEmpty) errors['color'] = 'Color is required';
      if (_odoController.text.trim().isEmpty) errors['odo'] = 'Odometer reading is required';
      if (_chassisController.text.trim().isEmpty) errors['chassis'] = 'Chassis / VIN is required';
      if (_engineController.text.trim().isEmpty) errors['engine'] = 'Engine number is required';
    }

    if (_currentStep == 3) {
      if (_selectedServiceTypes.isEmpty) errors['serviceType'] = 'Select at least one service type';
    }

    setState(() {
      _fieldErrors.clear();
      _fieldErrors.addAll(errors);
    });

    if (errors.isNotEmpty) {
      final firstError = errors.values.first;
      ScaffoldMessenger.of(context).clearSnackBars();
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Row(
            children: [
              const Icon(Icons.error_outline, color: Colors.white, size: 18),
              const SizedBox(width: 10),
              Expanded(child: Text(firstError, style: const TextStyle(fontWeight: FontWeight.w600))),
            ],
          ),
          backgroundColor: const Color(0xFFEF4444),
          behavior: SnackBarBehavior.floating,
          shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(10)),
          margin: const EdgeInsets.all(16),
          duration: const Duration(seconds: 3),
        ),
      );
      return false;
    }
    return true;
  }

  void _prevStep() {
    if (_currentStep > 1) {
      setState(() {
        _currentStep--;
      });
    }
  }

  Future<void> _submitJobCard() async {
    String? signatureBase64;
    try {
      if (_sigController.isNotEmpty) {
        final bytes = await _sigController.toPngBytes();
        if (bytes != null) {
          signatureBase64 = base64Encode(bytes);
        }
      }
    } catch (e) {
      debugPrint("Error capturing signature: $e");
    }

    final newCard = JobCardModel(
      jcNumber: _jcNumber,
      jcDate: _jcDate,
      regNumber: _regController.text,
      make: _makeController.text,
      model: _modelController.text,
      year: _yearController.text,
      color: _colorController.text,
      odo: _odoController.text,
      chassis: _chassisController.text,
      engine: _engineController.text,
      fuelType: _fuelType,
      transmission: _transmission,
      custName: _custNameController.text,
      custPhone: _custPhoneController.text,
      custEmail: _custEmailController.text,
      custAddress: _custAddressController.text,
      saName: _saNameController.text,
      techName: _techNameController.text,
      contactPref: _contactPref,
      serviceTypes: List<String>.from(_selectedServiceTypes),
      complaints: List<String>.from(_selectedComplaints),
      complaintDetails: _complaintDetailsController.text,
      fuelLevel: _fuelLevel,
      hasSpareWheel: _hasSpareWheel,
      hasToolKit: _hasToolKit,
      hasJackKit: _hasJackKit,
      hasDocuments: _hasDocuments,
      inspectionStatuses: Map<String, bool>.from(_inspectionStatuses),
      otherIssues: _otherIssuesController.text,
      photoPaths: _photoBytes.map((k, v) => MapEntry(k, k)),  // pass labels only
      signaturePoints: [],
      signatureBase64: signatureBase64,
    );

    jobCardsNotifier.value = List.from(jobCardsNotifier.value)..add(newCard);

    if (!mounted) return;

    showDialog(
      context: context,
      barrierDismissible: false,
      builder: (BuildContext context) {
        return Dialog(
          backgroundColor: Colors.transparent,
          insetPadding: const EdgeInsets.symmetric(horizontal: 20, vertical: 20),
          child: SuccessDialog(
            card: newCard,
            onRestart: () {
              Navigator.of(context).pop();
              setState(() {
                _currentStep = 1;
                _regController.clear();
                _makeController.clear();
                _modelController.clear();
                _yearController.clear();
                _colorController.clear();
                _odoController.clear();
                _chassisController.clear();
                _engineController.clear();
                _custNameController.clear();
                _custPhoneController.clear();
                _custEmailController.clear();
                _custAddressController.clear();
                _saNameController.clear();
                _techNameController.clear();
                _complaintDetailsController.clear();
                _otherIssuesController.clear();
                _selectedServiceTypes.clear();
                _selectedComplaints.clear();
                _sigController.clear();
                _jcNumber = 'JC${10000000 + math.Random().nextInt(90000000)}';
                _jcDate = DateTime.now().toString().split('.')[0];
                _photoBytes.clear();
              });
            },
            onGoToHome: () {
              Navigator.of(context).pop(); // Pop Success Dialog
              if (Navigator.of(context).canPop()) {
                Navigator.of(context).pop(); // Pop JobCardWizardPage
              }
              navigationIndexNotifier.value = 0; // Redirect tab to Home Page
            },
          ),
        );
      },
    );
  }

