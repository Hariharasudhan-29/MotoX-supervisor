// --- CUSTOM STEPACTION PROGRESS BAR ---
class StepperProgressHeader extends StatelessWidget {
  final int currentStep;
  final int totalSteps;

  const StepperProgressHeader({
    super.key,
    required this.currentStep,
    required this.totalSteps,
  });

  @override
  Widget build(BuildContext context) {
    final theme = Theme.of(context);
    final isDark = theme.brightness == Brightness.dark;

    final stepDetails = [
      {'label': 'Customer', 'icon': Icons.person},
      {'label': 'Vehicle', 'icon': Icons.directions_car},
      {'label': 'Service', 'icon': Icons.build},
      {'label': 'Inspect', 'icon': Icons.camera_alt},
      {'label': 'Summary', 'icon': Icons.receipt_long},
    ];

    return Row(
      children: List.generate(totalSteps * 2 - 1, (index) {
        if (index.isOdd) {
          // Connection Line
          final lineIndex = index ~/ 2;
          final isCompleted = lineIndex < currentStep - 1;
          return Expanded(
            child: Container(
              height: 2.5,
              decoration: BoxDecoration(
                borderRadius: BorderRadius.circular(2),
                color: isCompleted
                    ? const Color(0xFF34D399)
                    : isDark
                        ? Colors.white.withOpacity(0.08)
                        : Colors.black.withOpacity(0.08),
              ),
            ),
          );
        } else {
          // Step Dot
          final stepIndex = index ~/ 2 + 1;
          final details = stepDetails[stepIndex - 1];
          final isActive = stepIndex == currentStep;
          final isCompleted = stepIndex < currentStep;

          Color borderCol = isDark ? Colors.white.withOpacity(0.08) : Colors.black.withOpacity(0.08);
          Color fillCol = theme.colorScheme.surface;
          Color iconCol = isDark ? Colors.white38 : Colors.black38;

          if (isActive) {
            borderCol = theme.colorScheme.primary;
            fillCol = theme.colorScheme.primary;
            iconCol = Colors.white;
          } else if (isCompleted) {
            borderCol = const Color(0xFF34D399);
            fillCol = const Color(0xFF34D399);
            iconCol = Colors.white;
          }

          return Column(
            children: [
              Container(
                width: 32,
                height: 32,
                decoration: BoxDecoration(
                  shape: BoxShape.circle,
                  color: fillCol,
                  border: Border.all(color: borderCol, width: 2),
                ),
                child: Center(
                  child: Icon(
                    details['icon'] as IconData,
                    size: 16,
                    color: iconCol,
                  ),
